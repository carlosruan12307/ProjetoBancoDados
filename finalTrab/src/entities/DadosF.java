package entities;

public class DadosF {
Fullbucket fullbucket;
Tabela tabela;
public DadosF(Fullbucket fullbucket, Tabela tabela) {
	super();
	this.fullbucket = fullbucket;
	this.tabela = tabela;
}
public Fullbucket getFullbucket() {
	return fullbucket;
}
public void setFullbucket(Fullbucket fullbucket) {
	this.fullbucket = fullbucket;
}
public Tabela getTabela() {
	return tabela;
}
public void setTabela(Tabela tabela) {
	this.tabela = tabela;
}


}
